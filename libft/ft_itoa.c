#include "libft.h"

char	*ft_itoa(int n)
{
	char	str[12];
	int		i;
	int		neg;

	i = 11;
	str[i] = 0;
	neg = 0;
	if (!n)
		str[--i] = '0';
	if (n < 0)
	{
		neg = 1;
		str[--i] = '0' - n % 10;
		n /= -10;
	}
	while (n % 10 || n / 10)
	{
		str[--i] = n % 10 + '0';
		n /= 10;
	}
	if (neg)
		str[--i] = '-';
	return (ft_strdup(&str[i]));
}
