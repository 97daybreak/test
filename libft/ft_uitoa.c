#include "libft.h"

char	*ft_uitoa(unsigned int n)
{
	char		str[12];
	short int	i;

	i = 11;
	str[i] = 0;
	if (!n)
		str[--i] = '0';
	while (n % 10 || n / 10)
	{
		str[--i] = n % 10 + '0';
		n /= 10;
	}
	return (ft_strdup(&str[i]));
}
